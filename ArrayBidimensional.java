
public class ArrayBidimensional {
	
	public static void main(String[] args) {
		
		int [][] matrix= {
				{2,3,5,53,62},
				{42,3,15,63,2},
				{25,3,56,3,02},
				{2,63,75,43,29},
		};
		
		
		for (int i = 0; i < matrix.length; i++) {
			for (int j = 0; j < matrix[i].length; j++) {
				System.out.print(matrix[i][j]+" ");
			}
			System.out.println("");
		}
		
	
		
		
		
	}

}
